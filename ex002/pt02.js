// Trabalhando com operadores

var A = 5;
var B = 10;

 // Matemáticos
console.log(A+B);       // Adição
console.log(A-B);       // subtração
console.log(A/B);       // Divisão
console.log(A**B);      // Potenciação
console.log(A%B);       // Módulo
console.log(--A);       // Incremento
console.log(--B);       // Decremento

// Lógicos
console.log(true&&true);        //E  ; and
console.log(true||false);       //Ou ; or
console.log(!false);            //Negação
//console.log(!!);              //(!)Dupla Negação

// Relacionais
console.log(A==B);          //Igual
console.log(A!=B);          //Diferente
console.log(A<B);           //Menor
console.log(A>B);           //Maior
console.log(A<=B);          //Menor ou Igual
console.log(A>=B);          //Maior ou Igual
console.log(A===B);         //Idêntico
console.log(A!==B);         //Não Idêntico
// Condicionais

var n1 = 4;
var n2 = 8;

if (n1 > n2) 
    console.log("Maior\n");
else if (n1 == n2)
    console.log("Igual\n");
else 
    console.log("Menor\n");


if (n1-1 == n2)
    console.log("Sucessor");
else if (n1+1 == n2)
    console.log("Antecessor");
else if (n1 / n2 == 2)
    console.log("Dobro");
else if (n1 / n2 == 3)
    console.log("Triplo");
else if (n1 * 2 == n2)
    console.log("Metade");
else
    console.log("Sem relação"); 